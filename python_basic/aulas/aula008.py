'''
OP          SIGNIFICADO         EXEMPLO (TRUE)
>           MAIOR               2 > 1
>=          MAIOR OU IGUAL      2 >= 2s
<           MENOR               1 < 2
<=          MENOR OU IGUAL      2 <= 2
==          IGUAL               2 == 2 OU 'A' = 'A'
!=          DIFERENTE           2 != 3 OU 'A' != 'a'
'''


maior = 2 > 1
maior_ou_igual  = 2 >= 2
menor = 1 < 2
menor_ou_igual = 3 <= 4
igual = 'a' == 'a'
diferente = 'A' != 'a'

print(
    maior, maior_ou_igual, menor, menor, igual, diferente
)

# Operadores de comparação, sempre retorna um valor boolean (True or False)

